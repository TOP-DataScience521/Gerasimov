number = input('Введите целое число: ')

# ИСПРАВИТЬ: одинаковые операции в большинстве случаев имеет смысл выполнить заранее
number_a = int(number) + 1
number_b = int(number) - 1

print(f"Следующее за числом {number} число: {number_a} {'\n'}Для числа {number} предыдущее число: {number_b}")


